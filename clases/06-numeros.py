numero_entero = 2  # integer
numero_decimal = 4.5  # float

print(numero_entero + numero_decimal)  # suma
print(numero_entero - numero_decimal)  # resta
print(numero_entero * numero_decimal)  # multiplicacion
print(numero_entero / numero_decimal)  # division
print(numero_entero // numero_decimal)  # division sin decimales


# Modulo (Devuelve el resto lo que sobra de la division)
print(6 % 2)

# Esta es una forma de elevar a la potencia
print(2 ** 6)

