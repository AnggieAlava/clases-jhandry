import math

print(round(1.5))  # redondear


# Va a tomar el numero y va a llevarlo al superior entero mas cercao "ceil"
print(math.ceil(1.2))

# Va a tomar el numero y lo lleva a su inmediato inferior "floor"
print(math.floor(1.999))

# "pow" nos permite elevar un numero a la potencia de algo
print(math.pow(2, 8))

# "sqrt" nos permite sacar la raiz cuadrada del numero que nosotros le pasemos
print(math.sqrt(9))


